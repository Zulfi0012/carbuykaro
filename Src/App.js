function renderMarkdown(md) {
  let html = md
    .replace(/^###### (.*?)$/gm, "<h6>$1</h6>")
    .replace(/^##### (.*?)$/gm, "<h5>$1</h5>")
    .replace(/^#### (.*?)$/gm, "<h4>$1</h4>")
    .replace(/^### (.*?)$/gm, "<h3>$1</h3>")
    .replace(/^## (.*?)$/gm, "<h2>$1</h2>")
    .replace(/^# (.*?)$/gm, "<h1>$1</h1>")
    .replace(/\*\*\*(.*?)\*\*\*/gm, "<b><i>$1</i></b>")
    .replace(/\*\*(.*?)\*\*/gm, "<b>$1</b>")
    .replace(/\*(.*?)\*/gm, "<i>$1</i>")
    .replace(/!\[(.*?)\]\((.*?)\)/g, '<img alt="$1" src="$2" class="img-fluid my-3" style="max-height:400px;display:block;margin-left:auto;margin-right:auto;" />')
    .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener noreferrer">$1</a>')
    .replace(/`([^`]+)`/g, '<code class="bg-light rounded px-1">$1</code>')
    .replace(/((\|.+\|)\n)+(\|[-: ]+\|)+((.|\n)*?)(?=\n\n|\n#|$)/gm, match => {
      const rows = match.trim().split('\n').filter(Boolean);
      if (rows.length < 2) return match;
      const header = rows[0].split('|').filter(Boolean);
      const body = rows.slice(2).map(r => r.split('|').filter(Boolean));
      return `<table class="table table-bordered table-striped my-4"><thead><tr>${header.map(h => `<th>${h.trim()}</th>`).join('')}</tr></thead><tbody>${body.map(r => `<tr>${r.map(c => `<td>${c.trim()}</td>`).join('')}</tr>`).join('')}</tbody></table>`;
    })
    .replace(/^\s*-\s+(.*)$/gm, '<li>$1</li>')
    .replace(/(<li>.*<\/li>)/gs, '<ul>$1</ul>')
    .replace(/\n{2,}/g, "<br/><br/>")
    .replace(/\n/g, "<br/>");

  html = html.replace(/(<ul>)+/g, "<ul>").replace(/(<\/ul>)+/g, "</ul>");
  return { __html: html };
}

function Article({ title, date, content, cover, onBack }) {
  return (
    <div>
      <button className="btn btn-accent mb-4" onClick={onBack}>← Back to Articles</button>
      <img src={cover} alt={title} className="img-fluid rounded mb-3" style={{maxHeight: '300px', objectFit: 'cover', width: '100%'}}/>
      <h1 className="mb-3" style={{color: '#004b87'}}>{title}</h1>
      <div className="text-muted mb-4" style={{fontSize: '1.15rem'}}>{date}</div>
      <div style={{fontSize: '1.1rem'}} dangerouslySetInnerHTML={renderMarkdown(content)} />
    </div>
  );
}

